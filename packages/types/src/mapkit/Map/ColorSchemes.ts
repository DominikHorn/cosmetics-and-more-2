/**
 * Constants indicating the color scheme of the map.
 *
 * ### Overview
 * Color schemes apply to maps that have a standard or muted standard map type
 * (mapkit.Map.MapTypes.Standard or mapkit.Map.MapTypes.MutedStandard).
 * Use these constants with the map's colorScheme property.
 */
export enum ColorSchemes {
  /**
   * A constant indicating a light color scheme.
   */
  Light = "Light",

  /**
   * A constant indicating a dark color scheme.
   */
  Dark = "Dark",
}
